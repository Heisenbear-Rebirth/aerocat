"""
Analyze P0 (cross-task OOD evaluation) results.

Loads T2-trained and T3-trained OOD eval JSONs and the existing T1 OOD eval,
aggregates per-(task, group, lambda) crash_rate and success_rate across the
5 seeds, and emits:

  1. A markdown table per task summarizing crash_rate and success_rate at all lambdas
  2. A 3-panel IEEE-styled crash-rate-vs-lambda figure (T1 | T2 | T3)
  3. A 3-panel IEEE-styled success-rate-vs-lambda figure
  4. A console verdict: does the within-dense ordering of T1 (vanilla < Cai < PSC-dense
     at lambda=1) hold on T2 and T3?  This is the C1 cross-task verification.

Output:
  experiments/_p0_analysis/p0_table.md
  experiments/_p0_analysis/fig_ood_crosstask_crash.{pdf,png}
  experiments/_p0_analysis/fig_ood_crosstask_success.{pdf,png}
  experiments/_p0_analysis/p0_verdict.md
"""
from __future__ import annotations
import json
from pathlib import Path
from collections import defaultdict
import numpy as np
import matplotlib.pyplot as plt

_DEFAULT_BASE = Path(__file__).resolve().parents[2] / "experiments_runtime"
BASE = Path(__import__("os").environ.get("AEROCAT_EXP_BASE", str(_DEFAULT_BASE)))
OUT = BASE / "_p0_analysis"
OUT.mkdir(exist_ok=True)

INPUT_FILES = {
    "T1": BASE / "_dr_eval_results.json",
    "T2": BASE / "_dr_eval" / "T2_dr_eval_results.json",
    "T3": BASE / "_dr_eval" / "T3_dr_eval_results.json",
}

LAMBDAS = [0.0, 0.3, 0.5, 0.7, 1.0]
GROUP_FAMILY = {"A": "dense", "B": "sparse", "C": "dense",
                "D": "sparse", "E": "sparse", "F": "dense"}
GROUP_DESC = {
    "A": "MLP+dense (vanilla)",
    "B": "MLP+sparse",
    "C": "PSC+dense",
    "D": "PSC+sparse (ours)",
    "E": "PSC+sparse, w frozen",
    "F": "Cai 2025 dual-critic",
}


def load(path: Path):
    """Load JSON, return list of records with 'group','seed','lambda','crash_rate','success_rate'."""
    if not path.exists():
        return []
    return json.load(open(path))


def aggregate(records):
    """records -> {(group, lambda): {'crash': [..], 'success': [..]}}, 5 seeds."""
    acc = defaultdict(lambda: {"crash": [], "success": []})
    for r in records:
        if "error" in r:
            continue
        key = (r["group"], round(float(r["lambda"]), 3))
        acc[key]["crash"].append(float(r.get("crash_rate", 0.0)))
        acc[key]["success"].append(float(r.get("success_rate", 0.0)))
    return acc


def mean_sd(xs):
    if not xs: return (float("nan"), float("nan"), 0)
    a = np.asarray(xs)
    return float(a.mean()), float(a.std(ddof=1) if len(a) > 1 else 0.0), len(a)


# ---------- load + aggregate all tasks ----------
data = {}
for task, p in INPUT_FILES.items():
    recs = load(p)
    data[task] = aggregate(recs)
    print(f"[*] {task}: {len(recs)} eval records loaded from {p.name}")

# ---------- markdown table ----------
md = []
md.append("# P0 Cross-Task OOD Evaluation — Results\n")
md.append("Generated by `_scripts/analyze_p0_results.py`. Crash_rate and success_rate are 5-seed means; SD shown in parentheses.\n")

for task in ["T1", "T2", "T3"]:
    if not data[task]:
        md.append(f"\n## {task} — (no data)\n"); continue
    groups_in_task = sorted({g for g, _ in data[task].keys()})
    md.append(f"\n## {task}\n")

    # Crash rate table
    md.append("### Crash rate (5-seed mean ± SD)\n")
    header = "| Group |" + "".join(f" λ={l:.1f} |" for l in LAMBDAS)
    sep    = "|:-:|" + ":-:|" * len(LAMBDAS)
    md.append(header); md.append(sep)
    for g in groups_in_task:
        row = [f"| **{g}** |"]
        for l in LAMBDAS:
            m, s, n = mean_sd(data[task].get((g, l), {"crash": []})["crash"])
            row.append(f" {m:.3f}±{s:.3f} |" if n > 0 else " — |")
        md.append("".join(row))

    # Success rate table
    md.append("\n### Success rate (5-seed mean ± SD)\n")
    md.append(header); md.append(sep)
    for g in groups_in_task:
        row = [f"| **{g}** |"]
        for l in LAMBDAS:
            m, s, n = mean_sd(data[task].get((g, l), {"success": []})["success"])
            row.append(f" {m:.3f}±{s:.3f} |" if n > 0 else " — |")
        md.append("".join(row))

(OUT / "p0_table.md").write_text("\n".join(md), encoding="utf-8")
print(f"[+] wrote {OUT/'p0_table.md'}")


# ---------- verdict: within-dense crash ordering at lambda=1 ----------
verdict_md = ["# P0 Verdict — C1 Cross-Task Verification\n"]
verdict_md.append("**Question**: Does the within-dense crash-rate ordering observed on T1 at λ=1 generalize to T2 and T3?\n")
verdict_md.append("**T1 reference (from existing 5-seed eval)**: A=0.358, C=0.492, F=0.408 — PSC-dense (C) crashes most; vanilla (A) crashes least; Cai (F) intermediate.\n")
verdict_md.append("\n| Task | A (vanilla) | C (PSC-dense) | F (Cai dual) | Sparse families (if available) | Pattern |")
verdict_md.append("|:--:|:--:|:--:|:--:|:--:|:--:|")

def m(task, g):
    v = data[task].get((g, 1.0), {"crash": []})["crash"]
    return mean_sd(v)[0] if v else float("nan")

for task in ["T1", "T2", "T3"]:
    a, c, f = m(task, "A"), m(task, "C"), m(task, "F")
    b = m(task, "B"); d = m(task, "D")
    sparse_str = []
    if not np.isnan(b): sparse_str.append(f"B={b:.3f}")
    if not np.isnan(d): sparse_str.append(f"D={d:.3f}")
    sparse_col = ", ".join(sparse_str) if sparse_str else "—"
    # Pattern check: dense max - dense min, sparse max (if any) < dense min
    dense_vals = [v for v in (a, c, f) if not np.isnan(v)]
    sparse_vals = [v for v in (b, d) if not np.isnan(v)]
    pattern = ""
    if dense_vals and sparse_vals:
        if max(sparse_vals) < min(dense_vals):
            pattern = "✅ ALL sparse < ALL dense"
        else:
            pattern = f"⚠ overlap: sparse_max={max(sparse_vals):.3f} vs dense_min={min(dense_vals):.3f}"
    elif dense_vals:
        pattern = f"dense range {min(dense_vals):.3f}–{max(dense_vals):.3f}"
    verdict_md.append(f"| {task} | {a:.3f} | {c:.3f} | {f:.3f} | {sparse_col} | {pattern} |")

verdict_md.append("\n## Interpretation\n")
verdict_md.append("- If pattern is ✅ on all three tasks, C1 (reward composition dominance) is verified cross-task.")
verdict_md.append("- Within-dense ordering (A vs C vs F) gives evidence on whether 'critic-refinement-hurts-safety' is task-dependent or universal.")
(OUT / "p0_verdict.md").write_text("\n".join(verdict_md), encoding="utf-8")
print(f"[+] wrote {OUT/'p0_verdict.md'}")


# ---------- IEEE-styled figures ----------
plt.rcParams.update({
    "font.family": "serif",
    "font.serif": ["Times New Roman", "DejaVu Serif", "Liberation Serif"],
    "font.size": 8, "axes.labelsize": 8, "axes.titlesize": 9,
    "legend.fontsize": 6.5, "xtick.labelsize": 7, "ytick.labelsize": 7,
    "pdf.fonttype": 42, "ps.fonttype": 42,
    "axes.linewidth": 0.6, "lines.linewidth": 1.0, "lines.markersize": 3,
    "savefig.bbox": "tight", "savefig.pad_inches": 0.02,
})

COLORS = {"A": "#1f77b4", "B": "#d62728", "C": "#2ca02c",
          "D": "#9467bd", "E": "#8c564b", "F": "#ff7f0e"}
MARKERS = {"A": "o", "B": "s", "C": "^", "D": "v", "E": "D", "F": "x"}
LS_FAM = {"dense": "-", "sparse": "--"}


def plot_metric(metric: str, ylabel: str, fname: str):
    fig, axes = plt.subplots(1, 3, figsize=(7.16, 2.5), sharey=True)
    for ax, task in zip(axes, ["T1", "T2", "T3"]):
        if not data[task]:
            ax.set_title(f"{task} (no data)"); continue
        groups_in_task = sorted({g for g, _ in data[task].keys()})
        for g in groups_in_task:
            means = []; sds = []
            for l in LAMBDAS:
                vs = data[task].get((g, l), {metric: []})[metric]
                m_, s_, n_ = mean_sd(vs)
                means.append(m_); sds.append(s_)
            means = np.asarray(means); sds = np.asarray(sds)
            fam = GROUP_FAMILY[g]
            ax.errorbar(LAMBDAS, means, yerr=sds,
                        color=COLORS[g], marker=MARKERS[g], linestyle=LS_FAM[fam],
                        linewidth=1.0, markersize=4, capsize=2,
                        label=f"{g} ({fam[0]})")
        ax.set_title(task)
        ax.set_xlabel(r"Test-time DR $\lambda$")
        ax.set_xticks(LAMBDAS)
        ax.grid(True, alpha=0.3, linewidth=0.4)
        ax.tick_params(width=0.5, length=2.5)
        for spine in ax.spines.values():
            spine.set_linewidth(0.6)
        ax.legend(loc="best", framealpha=0.85, handlelength=2.2, borderpad=0.3,
                  labelspacing=0.25, ncol=2)
    axes[0].set_ylabel(ylabel)
    plt.tight_layout()
    fig.savefig(OUT / f"{fname}.pdf")
    fig.savefig(OUT / f"{fname}.png", dpi=600)
    plt.close(fig)
    print(f"[+] wrote {OUT/(fname+'.pdf')} and .png")

plot_metric("crash",   "OOD crash rate (5-seed mean ± SD)",   "fig_ood_crosstask_crash")
plot_metric("success", "OOD success rate (5-seed mean ± SD)", "fig_ood_crosstask_success")

# Copy figures into _paper_figs for draft to reference
import shutil
for f in ["fig_ood_crosstask_crash", "fig_ood_crosstask_success"]:
    for ext in ("pdf", "png"):
        src = OUT / f"{f}.{ext}"
        dst = BASE / "_paper_figs" / f"{f}.{ext}"
        shutil.copy2(src, dst)
        print(f"[+] copied {dst.name} -> _paper_figs/")
